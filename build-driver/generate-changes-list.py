#!/usr/bin/env python3
import os
import subprocess
import sys
import re
from pathlib import Path


SECTION_SEPARATOR = f"{'-' * 72}\n"


class Listener:
    def __init__(self):
        self.failed = False

    def error(self, message: str):
        raise NotImplementedError

    def info(self, message: str):
        raise NotImplementedError

    def warn(self, message: str):
        raise NotImplementedError


class CliListener(Listener):
    def error(self, message: str):
        self.failed = True
        sys.stderr.write(f"E: {message}\n")

    def info(self, message: str):
        sys.stdout.write(f"I: {message}\n")

    def warn(self, message: str):
        sys.stdout.write(f"W: {message}\n")


def parse_package_list(s: str) -> dict:
    package_dict = {}
    for line in s.split("\n"):
        match = re.match(r"^ii\s+(\S+)\s+(\S+)\s", line)
        if match:
            package_dict[match[1]] = match[2]
    return package_dict


def git_repo_clone_and_update(local_git_path: Path, remote_url: str):
    # Disable any input prompting from git.
    env = dict(os.environ) | {"GIT_TERMINAL_PROMPT": "0"}

    if not local_git_path.exists():
        git_command = ["git", "clone", "--bare", "--single-branch", remote_url, str(local_git_path)]
        subprocess.run(git_command, env=env)
        if not local_git_path.exists():
            raise Exception("Repository not found after git clone")

    # update repo, in case it existed already (cache).
    subprocess.run(
        ["git", "remote", "set-url", "origin", remote_url],
        cwd=local_git_path,
        env=env,
    )
    subprocess.run(
        ["git", "remote", "update", "--prune"],
        cwd=local_git_path,
        env=env,
    ).check_returncode()


def git_get_changes(local_git_path: Path, range) -> str:
    """Get git log --online output. On failure, a failure string is returned."""

    result = subprocess.run(
        ["git", "log", "--oneline", range],
        cwd=local_git_path,
        capture_output=True,
    )

    if result.returncode != 0:
        git_changes = "(failed)"
    else:
        git_changes = "\n    ".join(result.stdout.decode().splitlines())

    return git_changes


def generate_git_package_changes(
    package: str,
    old_version: str | None,
    version: str,
    git_url_base: str,
    git_repo_workspace: Path,
    listener: Listener
) -> list[str]:
    """Generate changelog for a package that we believe is in our git."""

    listener.info(
        f"Generating changes list for package {package}, "
        f"Version {old_version} -> {version}"
    )

    try:
        # clone repo
        git_url = f"{git_url_base}/{package}"
        gitpath = git_repo_workspace / f"{package}.git"
        git_repo_clone_and_update(gitpath, git_url)

        if old_version:
            range = f"v{old_version}..v{version}"
        else:
            range = f"v{version}"

        git_changes = git_get_changes(gitpath, range)

        return [
            f"Package {package}: {range} {'(new)' if not old_version else ''}\n",
            f"    {git_changes}\n",
            SECTION_SEPARATOR,
        ]
    except Exception as e:
        listener.warn(f"Generating change report for package {package} failed: {e}")
        return []


def build_changes(
    output_filename: Path,
    dpkg_list_new: Path,
    dpkg_list_old: Path,
    package_prefix: str,
    git_url_base: str,
    git_repo_workspace: Path,
    job_name: str,
    build_id: str,
    listener: Listener,
):
    git_repo_workspace.mkdir(parents=True, exist_ok=True)

    changelog_parts = [
        SECTION_SEPARATOR,
        f"Generated by CI for job {job_name} {build_id}\n",
        SECTION_SEPARATOR,
    ]

    if not dpkg_list_new.exists():
        listener.error(f"Could not read package list {dpkg_list_new}")
        return

    packages = parse_package_list(dpkg_list_new.read_text())
    packages_old = {}
    try:
        packages_old = parse_package_list(dpkg_list_old.read_text())
    except Exception as e:
        listener.info(f"While parsing old package list: {e}")

    debian_packages_removed = []
    debian_packages_added = []
    debian_packages_changed = []

    # Show removed packages first.
    for package in set(packages_old) - set(packages):
        if re.match(f"^{package_prefix}", package):
            changelog_parts += [f"Package {package}: Removed.\n", SECTION_SEPARATOR]
        else:
            debian_packages_removed.append(package)

    # Now show changed and added packages.
    for package, version in packages.items():
        old_version = packages_old.get(package)
        if old_version and old_version == version:
            continue

        if re.match(f"^{package_prefix}", package):
            changelog_parts += generate_git_package_changes(
                package,
                old_version,
                version,
                git_url_base,
                git_repo_workspace,
                listener
            )
        else:
            # Debian-originated package
            if old_version:
                debian_packages_changed.append(f"{package} {old_version} -> {version}")
            else:
                debian_packages_added.append(package)

    def changes_to_lines(changes):
        return "\n    ".join(changes).strip()

    changelog_parts += [
        "Changes to Debian package list:\n",
        "  Added:\n",
        f"    {changes_to_lines(debian_packages_added)}\n",
        "  Changed:\n",
        f"    {changes_to_lines(debian_packages_changed)}\n",
        "  Removed:\n",
        f"    {changes_to_lines(debian_packages_removed)}\n",
        SECTION_SEPARATOR,
    ]

    output_filename.write_text("".join(changelog_parts))


def main() -> int:
    if len(sys.argv) != 9:
        print(
            f"Usage: {sys.argv[0]} output_filename dpkg_list_new dpkg_list_old package_prefix git_url_base git_repo_workspace job_name build_id"
        )
        return 2

    listener = CliListener()
    try:
        build_changes(
            Path(sys.argv[1]),
            Path(sys.argv[2]),
            Path(sys.argv[3]),
            sys.argv[4],
            sys.argv[5],
            Path(sys.argv[6]),
            sys.argv[7],
            sys.argv[8],
            listener,
        )
    except Exception as except_inst:
        listener.error(f"Uncaught exception: {except_inst}")

    if listener.failed:
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
